    private void tb_DtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_DtMouseClicked
        
        txt_Thuoc.setText("");
        if (tb_Dt.getSelectedRowCount() > 0){
            txt_Madt.setText((String) tb_Dt.getValueAt(tb_Dt.getSelectedRow(), 0));
            txt_Datedt.setText((String) tb_Dt.getValueAt(tb_Dt.getSelectedRow(), 1));
            txt_Tongtiendt.setText((String) tb_Dt.getValueAt(tb_Dt.getSelectedRow(), 2));

            String Madt = (String) tb_Dt.getValueAt(tb_Dt.getSelectedRow(), 0);
            txt_Thuoc.setText(String.format("|%-15s|%-10s|%n", "Tên thuốc", "SL"));
            ArrayList<ArrayList<Object>> click_Dt;

            click_Dt = new sql_Benhnhan(Mabn).click_Dt(Madt);
            for (ArrayList<Object> x : click_Dt) {
                txt_Thuoc.setText(txt_Thuoc.getText() + String.format("|%15s|%10s|%n", x.get(0), x.get(1)));
            }
        }
        btn_Searchdt.setEnabled(false);     
    }//GEN-LAST:event_tb_DtMouseClicked

