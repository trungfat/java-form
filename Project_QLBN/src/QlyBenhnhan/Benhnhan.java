    private void tb_BaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_BaMouseClicked
        // TODO add your handling code here:
        if(tb_Ba.getSelectedRowCount() > 0){
            txt_Maba.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 0));
            txt_Datekham.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 2));
            txt_Tongtien.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 3));
            
            ArrayList<String> click_Ba = new sql_Benhnhan(Mabn).click_Ba(txt_Maba.getText());
            txt_Tenbs.setText(click_Ba.get(0));
            txt_Chandoan.setText(click_Ba.get(1));
            
            btn_Searchba.setEnabled(false);
        }   
    }//GEN-LAST:event_tb_BaMouseClicked

    private void btn_SearchbaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchbaActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tb_Ba.getModel();
        model.getDataVector().clear();
        ArrayList<ArrayList<Object>> sr_Ba = new sql_Benhnhan(Mabn).hienthi_SearchBa(txt_Searchba.getText());

        if(sr_Ba != null){
            for (ArrayList<Object> x : sr_Ba) {
                model.addRow(x.toArray());
            }
        }
        else{
            model.fireTableDataChanged();
        }
        txt_Searchba.setText("");
    }//GEN-LAST:event_btn_SearchbaActionPerformed

