    private void hienthi_Pro() throws ParseException{
        ArrayList<String> ht_Pro = new sql_Benhnhan(Mabn).hienthi_Pro();
        txt_Mabn.setText(Mabn);
        txt_Hoten.setText(ht_Pro.get(0));
        txt_Gtinh.setText(ht_Pro.get(1));
        
        txt_Date.setDate(new SimpleDateFormat("dd/MM/yyyy").parse(ht_Pro.get(2)));
        
        txt_Cccd.setText(ht_Pro.get(3));
        txt_Bhyt.setText(ht_Pro.get(4));
        txt_Sdt.setText(ht_Pro.get(5));
        txt_Email.setText(ht_Pro.get(6));
        txt_Diachi.setText(ht_Pro.get(7));  
        
        txt_Hoten.setEditable(false);
        txt_Gtinh.setEditable(false);
        txt_Date.setEnabled(false);
        txt_Cccd.setEditable(false);
        txt_Bhyt.setEditable(false);
        txt_Sdt.setEditable(false);
        txt_Email.setEditable(false);
        txt_Diachi.setEditable(false);
        btn_Save.setEnabled(false);
    }
    
    private void hienthi_Ba(){
        DefaultTableModel model = (DefaultTableModel) tb_Ba.getModel();
        model.getDataVector().clear();
        ArrayList<ArrayList<Object>> ht_Ba = new sql_Benhnhan(Mabn).hienthi_Ba();
        
        for (ArrayList<Object> x : ht_Ba) {
            model.addRow(x.toArray());
        }
        txt_Maba.setText("");
        txt_Tenbs.setText("");
        txt_Datekham.setText("");
        txt_Chandoan.setText("");
        txt_Tongtien.setText("");
        txt_Searchba.setText("");
        tb_Ba.clearSelection();
        txt_Maba.setEditable(false);
        txt_Tenbs.setEditable(false);
        txt_Datekham.setEditable(false);
        txt_Chandoan.setEditable(false);
        txt_Tongtien.setEditable(false);
        btn_Searchba.setEnabled(true);
    }
    
