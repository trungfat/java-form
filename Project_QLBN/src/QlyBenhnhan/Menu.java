    private void tb_BaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb_BaMouseClicked
        // TODO add your handling code here:
        if (tb_Ba.getSelectedRowCount() > 0){
            txt_Maba1.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 0));
            cbx_Mabn.setSelectedItem((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 1));
            cbx_Mabs.setSelectedItem((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 4));
            try {
                txt_Datenhap.setDate(new SimpleDateFormat("dd/MM/yyyy").parse((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 5)));
                txt_Datexuat.setDate(new SimpleDateFormat("dd/MM/yyyy").parse((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 6)));
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(null, "Định dạng ngày không đúng", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            txt_Chandoan.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 7));
            txt_Tongtien.setText((String) tb_Ba.getValueAt(tb_Ba.getSelectedRow(), 8));
            
            check_Bhyt();
            
            txt_Ttthuoc2.setText(new sql_Qly_Benhan().Click_Ba(txt_Maba1.getText()));
            if(cb_Bhyt.isSelected()){
                txt_Vienphi.setText("0");
            }
            else {
                int tt = Integer.parseInt(txt_Tongtien.getText());
                int ttdt = Integer.parseInt(txt_Ttthuoc2.getText());
                txt_Vienphi.setText(String.valueOf(tt - ttdt));
            }
            
            btn_Editba.setEnabled(true);
            btn_Deleteba.setEnabled(true);
            btn_Addba.setEnabled(false);
            btn_Searchba.setEnabled(false);
        }
    }//GEN-LAST:event_tb_BaMouseClicked
    
