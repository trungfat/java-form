    private void btn_SearchbnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SearchbnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tb_Bn.getModel();
        model.getDataVector().clear();
        ArrayList<ArrayList<Object>> sr_bn = new sql_Qly_Benhnhan().search_Bn(txt_Searchbn.getText());
        if (sr_bn == null) {
                //cập nhật lại dữ liệu của bảng
            model.fireTableDataChanged();
        } else {
            for (ArrayList<Object> x : sr_bn) {
                model.addRow(x.toArray());
            }
        }  

        txt_Searchbn.setText("");
    }//GEN-LAST:event_btn_SearchbnActionPerformed

